package day7.operators;

public class ArithmeticOperatorEx1 {

	public static void main(String[] args) {
		// Variables Definition and Initialization
		int number1 = 56, number2 = 10;
		//Addition Operation
		int res = number1+number2;
		System.out.println("Sum is: "+res);
		//Substraction Operaion
		 res =number1-number2;
		System.out.println("Substration is: "+res);
		//Multiplication Operation
		res = number1*number2;
		System.out.println("Multiplication is: "+res);
		//Division Operation
		 res = number1/number2;
		System.out.println("Div is: "+res);
		//Modulus Operation
		res = number1%number2;
		System.out.println("Mod is: "+res);

	}

}
