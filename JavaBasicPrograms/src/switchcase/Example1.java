package switchcase;

public class Example1 {

	public static void main(String[] args) {
		int value = 10;
		switch(value) {
		case 2:
			System.out.println("Congratulations here is the case 3 executed");
			break;
			
		case 6:
			System.out.println("Congratulations here is the case 6 executed");
			break;
			
		case 8:
			System.out.println("Congratulations here is the case 8 executed");
			break;
			
		case 10:
			System.out.println("Congratulations here is the case 10 executed");
			break;
			
		default:
			System.out.println("Congratulations here is the default executed");
			break;
		}
		
	}

}
