package switchcase;

public class Example2 {

	public static void main(String[] args) {
		char value = 'i';
		switch(value) {
		case 'a':
			System.out.println("Its a vowel");
			break;
		case 'e':
			System.out.println("Its a vowel");
			break;
		case 'i':
			System.out.println("Its a vowel");
			break;
		case 'o':
			System.out.println("Its a vowel");
			break;
		case 'u':
			System.out.println("Its a vowel");
			break;
		default:
			System.out.println("Its a alphabet");
			break;
		}

	}

}
