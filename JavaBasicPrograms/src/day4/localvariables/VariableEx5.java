package day4.localvariables;

public class VariableEx5 {

	public static void main(String[] args) {
		System.out.println("Program Starts");
		//syntax to declare & initlize a variable: datatype variablename = value;
		// 25 + 15 =30
		int num1=25;
		int num2=15;
		int result=num1+num2;
		System.out.println("Number 1 is : "+num1);
		System.out.println("Number 2 is : "+num2);
		System.out.println("Addition of two number is : "+result);
		System.out.println("Program ends");
		
		// 2. subtraction 
		num1 = 65;
		num2 = 35;
		result = num1-num2;
		System.out.println("Number 1 is : "+num1);
		System.out.println("Number 2 is : "+num2);
		System.out.println("Substraction of two number is : "+result);
		System.out.println("Program ends");
		
		// 3. division(/)
		num1 = 75;
		num2 = 15;
		result = num1/num2;
		System.out.println("Number 1 is : "+num1);
		System.out.println("Number 2 is : "+num2);
		System.out.println("Division of two number is : "+result);
		System.out.println("Program ends");
		
		//4.mod(%)
		num1 = 85;
		num2 = 10;
		result = num1%num2;
		System.out.println("Number 1 is : "+num1);
		System.out.println("Number 2 is : "+num2);
		System.out.println("Mod of two number is : "+result);
		System.out.println("Program ends");
		
		// 5. multiplication (*)
		num1 = 100;
		num2 = 10;
		result = num1*num2;
		System.out.println("Number 1 is : "+num1);
		System.out.println("Number 2 is : "+num2);
		System.out.println("Multiplication of two number is : "+result);
		System.out.println("Program ends");
		
	}

}
